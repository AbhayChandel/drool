package com.hexlindia.drool.user.data.doc;

public class VideoLike {

    private String id;
    private String title;

    public VideoLike(String id, String title) {
        this.id = id;
        this.title = title;
    }

    public String getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }
}
